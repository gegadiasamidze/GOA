print("აირჩიეთ ოპერაცია:")
print("1. დეპოზიტი (ფულის ჩარიცხვა)")
print("2. განაღდება (ფულის ამოღება)")
print("3. ნახვა (ბალანსის ნახვა)")
print("4. გასვლა")

choice = input("შეიყვანეთ ოპერაციის ნომერი: ")

# ოპერაციის შესრულება
if choice == "1":  # დეპოზიტი
    deposit = float(input("შეიყვანეთ თანხა დეპოზიტისთვის: "))
    balance += deposit
    print(f"თქვენი დეპოზიტი შესრულდა! ახალი ბალანსი: {balance} ლარი.")
elif choice == "2":  # განაღდება
    withdraw = float(input("შეიყვანეთ თანხა განაღდებისთვის: "))
    if withdraw > balance:
        print("მონიშნული თანხა მეტია ვიდრე თქვენი ბალანსი!")
    else:
        balance -= withdraw
        print(f"თქვენი განაღდება წარმატებით შესრულდა! ახალი ბალანსი: {balance} ლარი.")
elif choice == "3":  # ნახვა
    print(f"თქვენი ბალანსი: {balance} ლარი.")
elif choice == "4":  # გასვლა
    print("გთხოვთ, მოემზადეთ გასვლისთვის!")
else:
    print("არასწორი არჩევანი. გთხოვთ, სცადოთ ისევ.")